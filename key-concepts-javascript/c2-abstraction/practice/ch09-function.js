/* =====09. 함수 부려먹기 III===== */
function calculateRectangleArea(width, height) {
  return width * height;
}

let area1 = calculateRectangleArea(3, 4);
let area2 = calculateRectangleArea(5, 8);
let area3 = calculateRectangleArea(7, 2);

console.log(`Area1: ${area1}, Area2: ${area2}, Area3: ${area3}`);
