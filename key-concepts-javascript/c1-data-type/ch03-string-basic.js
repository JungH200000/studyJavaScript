// JavaScript에서 숫자를 표현하고 싶다면 그냥 숫자만 작성하면 된다.
console.log(7);
console.log(3.14);

/** 문자열(String)
 * 문자를 표현할 때 사용하는 자료형
 * 따옴표를 사용하면 글자가 문자열이라는 형태의 값으로 인식될 수 있다.
 * 따옴표는 문자열을 구분하기 위해 사용하는 기호로, 출력할 때 표시되지 않는다.
 * '안녕' = "안녕"
 *
 * > 주의
 * 문자열은 반드시 같은 따옴표로 감싸줘야 한다.
 * 서로 다르다면 같은 따옴표를 찾을 때까지 문자열로 계속 인식된다.
 * ex) '안녕", "배고파'
 */

// console.log(안녕하세요.); => 오류
console.log('안녕하세요.'); // 안녕하세요.
