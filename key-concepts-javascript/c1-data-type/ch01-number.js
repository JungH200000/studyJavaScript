/* =====01. 숫자형===== */
// JavaScript는 숫자형에 기본적인 사칙연산이 가능하다.

// 덧셈
console.log(1 + 8); // 9
// 뺄셈
console.log(6 - 7); // -1
// 곱셈
console.log(2 * 2); // 4
// 나눗셈
console.log(5 / 2); // 2.5

// 나머지 연산
console.log(7 % 3); // 1

// 거듭제곱
console.log(3 ** 4); // 3의 4제곱 = 3 * 3 * 3 * 3 = 81
console.log(3 * 3 * 3 * 3); // 81

// 연산
console.log(2 + 3 * 5); // 2 + 3 * 5 = 2 + 15 = 17
console.log((2 + 3) * 5); // (2 + 3) * 5 = 5 * 5 = 25
