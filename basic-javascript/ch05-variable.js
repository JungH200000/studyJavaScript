/*=====05. 변수=====*/
/** 변수(Variable)
 * 프로그래밍에서는 값에 이름 부여하기 위해 변수라는 것을 활용한다.
 * 값을 저장할 수 있는 메모리(저장) 공간으로, 이 값은 변할 수 있다.
 */

// 1.변수 선언
let espressPrice = 3000; // =(등호) 연산자 : 오른쪽에 있는 값을 왼쪽에 할당하는 역할
// 이렇게 하면 expressPrice라는 변수를 입력하면 3000이라는 숫자를 사용하는 것과 같은 의미가 된다.

console.log(espressPrice);

/** 아래처럼 작성 가능
 * let espressPrice;
 * espressPrice = 3000;
 */

// 예시
let latterPrice = 4300;
let mochaPrice = 4800;

console.log(espressPrice * 2);
console.log(latterPrice * 4 + mochaPrice * 2);
console.log(latterPrice + mochaPrice);
console.log(espressPrice + latterPrice * 4 + mochaPrice * 2);
console.log(latterPrice * 3);
