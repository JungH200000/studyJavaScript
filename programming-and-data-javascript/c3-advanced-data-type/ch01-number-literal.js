/* =====01. 다양한 숫자 표기법===== */

/**
 * ## 1. 지수 표기법 (Exponential Notation)
 * 아주 큰 숫자나 0에 가까운 작은 소수를 간결하게 표현할 때 사용한다.
 * 알파벳 'e' 앞의 숫자에, 'e' 뒤의 숫자만큼 10의 거듭제곱을 곱한 값을 의미한다.
 *
 * - 1e9 -> 1 * (10 ** 9) -> 10억
 * - 16e-5 -> 16 * (10 ** -5) -> 0.00016
 */
console.log(1000000000); // 1000000000
console.log(1e9); // 1000000000

console.log(1e9 === 1000000000); // true
console.log(16e-5 === 0.00016); // true

/**
 * ## 다른 진법 표기법
 * 10진법 외에도 16진법, 8진법, 2진법 리터럴을 코드에 직접 사용할 수 있다.
 * 어떤 진법으로 작성하든, JavaScript는 모두 10진수 숫자로 인식하여 계산한다.
 */

// ### 1) 16진법 (Hexadecimal) : '0x' 접두사
// 0~9, a~f까지 사용
let hex = 0xff; // 255
console.log(hex);

// ### 2) 8진법 (Octal) : '0o' 접두사
// 0~7까지 사용
let octal = 0o377; // 255
console.log(octal);

// ### 3) 2진법 (binary numeral system) : '0b' 접두사
// 0과 1만 사용 (데이터의 bit 연산 등에 사용됨)
let binary = 0b11111111; // 255
console.log(binary);
